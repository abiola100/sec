$(document).ready(function(){
    function closeElement(){
        if($(window).width()<= 720){
            $('.me').hide();
            $('.nav-toggle').click(function(){
                $('.me').toggle();
            });
        }
        else{
            $('.me').show();
        }
    }
    closeElement()
    $(window).resize(function(){
        closeElement();
    });
})
